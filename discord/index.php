<p>Now here is the full story:</p>

<hr>

<p>
Swamp Servers, a Garry's Mod gaming community, is a relic of the time when the internet used to be free.
Within the game, I allow our users to say basically whatever they want short of calling for violence or extreme edgyness, so our users tend to have a very "libertarian" view of what they presume to be allowed.
This has made it difficult to operate a Discord guild, as in my experience Discord's policy is that whenever someone says something in violation of Discord's ToS,
they not only take action against that user, but they also take action against the guild they posted in and that guild owner's account.
</p>

<p>
We've had our guild taken down several times, and after the first time I lost my personal account because of what someone else said, I wisened up and started using alt accounts to make the new guilds.
Every time the guild is removed, so is the owner's account. When the takedown occurs, I get an email like this:
